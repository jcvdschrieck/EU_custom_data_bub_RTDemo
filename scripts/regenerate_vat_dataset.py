"""
Regenerate lib/vat_dataset.py from Context/VAT_Cases_Generated_17042026_6.xlsx.

Run after updating the source xlsx. Idempotent — overwrites the target file.
The generated module is imported by the new vat_ratio engine (Stage 2) and
the new seeder (Stage 3); the legacy lib/catalog.py is not touched.
"""
from __future__ import annotations

from collections import defaultdict
from pathlib import Path

import pandas as pd

ROOT      = Path(__file__).resolve().parent.parent
XLSX_PATH = ROOT / "Context" / "VAT_Cases_Generated_17042026_6.xlsx"
OUT_PATH  = ROOT / "lib"     / "vat_dataset.py"


def main() -> None:
    tx  = pd.read_excel(XLSX_PATH, sheet_name="VAT Missclassification Last")
    ref = pd.read_excel(XLSX_PATH, sheet_name="VAT Master Ref")

    # ── 1. Parent → subcategories ───────────────────────────────────────────
    parents: dict[str, list[tuple[str, str]]] = defaultdict(list)
    for _, r in ref.iterrows():
        parents[r["Category"]].append((r["VAT Category ID"], r["EU VAT Category Name"]))
    parents_ordered = sorted(parents.items())

    # ── 2. (dest, subcat) → (rate, rate_type) ───────────────────────────────
    rate_lookup: dict[tuple[str, str], tuple[float, str]] = {}
    for _, r in tx.iterrows():
        key = (r["Destination Country"], r["VAT Code (Recommended)"])
        val = (float(r["VAT Rate (Recommended)"]) / 100.0,
               r["VAT Rate Type (Recommeded)"])
        if key in rate_lookup:
            assert rate_lookup[key] == val, f"rate conflict at {key}"
        rate_lookup[key] = val

    # ── 3. Per-country rates by rate-type (S/R1/SR/Z) ───────────────────────
    country_rates: dict[str, dict[str, float]] = {}
    for dest in sorted(tx["Destination Country"].unique()):
        sub = tx[tx["Destination Country"] == dest]
        rates_by_type: dict[str, float] = {}
        for _, r in sub.iterrows():
            rates_by_type[r["VAT Rate Type (Recommeded)"]] = (
                float(r["VAT Rate (Recommended)"]) / 100.0
            )
        country_rates[dest] = rates_by_type

    # ── 4. Sellers ──────────────────────────────────────────────────────────
    sellers: list[dict] = []
    for i, name in enumerate(sorted(tx["Seller"].unique()), start=1):
        sub = tx[tx["Seller"] == name]
        origins = sorted(sub["Origin Country"].unique())
        assert len(origins) == 1, f"{name} has multiple origins: {origins}"
        sellers.append({
            "id":                 f"SEL{i:03d}",
            "name":               name,
            "origin":             origins[0],
            "destinations":       sorted(sub["Destination Country"].unique()),
            "parent_categories":  sorted(sub["VAT Category (declared)"].unique()),
        })

    # ── Render ──────────────────────────────────────────────────────────────
    lines: list[str] = []
    lines.append('"""')
    lines.append('VAT reference dataset extracted from VAT_Cases_Generated_17042026_6.xlsx.')
    lines.append('')
    lines.append('Single source of truth for the new dataset (Stage 1 of the dataset')
    lines.append('rebuild). Loaded by the new vat_ratio engine (Stage 2) and the new')
    lines.append('seeder (Stage 3). Co-exists with the legacy lib/catalog.py until')
    lines.append('the legacy seeder is retired.')
    lines.append('')
    lines.append('Generated by: scripts/regenerate_vat_dataset.py')
    lines.append('Source xlsx:  Context/VAT_Cases_Generated_17042026_6.xlsx')
    lines.append('"""')
    lines.append('from __future__ import annotations')
    lines.append('')
    lines.append('')
    lines.append('# ── Destinations & origins ──────────────────────────────────────────────────')
    lines.append('')
    lines.append('DESTINATION_COUNTRIES: list[str] = ["DE", "FR", "IE", "NL"]')
    lines.append('')
    lines.append('ORIGIN_COUNTRIES: list[str] = ["CN", "IN"]')
    lines.append('')
    lines.append('ORIGIN_NAME_TO_CODE: dict[str, str] = {')
    lines.append('    "China": "CN",')
    lines.append('    "India": "IN",')
    lines.append('}')
    lines.append('')
    lines.append('COUNTRY_NAMES: dict[str, str] = {')
    lines.append('    "DE": "Germany",')
    lines.append('    "FR": "France",')
    lines.append('    "IE": "Ireland",')
    lines.append('    "NL": "Netherlands",')
    lines.append('    "CN": "China",')
    lines.append('    "IN": "India",')
    lines.append('}')
    lines.append('')
    lines.append('# ── VAT taxonomy: parent category → list of (subcategory_code, subcategory_name) ─')
    lines.append('')
    lines.append('VAT_CATEGORIES: dict[str, list[tuple[str, str]]] = {')
    for parent, subs in parents_ordered:
        lines.append(f'    {parent!r}: [')
        for code, name in sorted(subs):
            lines.append(f'        ({code!r}, {name!r}),')
        lines.append('    ],')
    lines.append('}')
    lines.append('')
    lines.append('# Flat lookup: subcategory code → (parent_category, subcategory_name)')
    lines.append('SUBCATEGORY_BY_CODE: dict[str, tuple[str, str]] = {')
    for parent, subs in parents_ordered:
        for code, name in sorted(subs):
            lines.append(f'    {code!r}: ({parent!r}, {name!r}),')
    lines.append('}')
    lines.append('')
    lines.append('# ── Per-(destination, subcategory) expected VAT rate ────────────────────────')
    lines.append('# Built from xlsx ground truth (recommended_rate). 78 of 216 (4 dest × 54 subcat)')
    lines.append('# pairs are observed; the rest fall back to country_standard_rate at lookup time.')
    lines.append('# Value is (rate_fraction, rate_type) where rate_type ∈ {"S", "R1", "SR", "Z"}.')
    lines.append('')
    lines.append('VAT_RATE_LOOKUP: dict[tuple[str, str], tuple[float, str]] = {')
    for (dest, code) in sorted(rate_lookup.keys()):
        rate, rtype = rate_lookup[(dest, code)]
        lines.append(f'    ({dest!r}, {code!r}): ({rate:.4f}, {rtype!r}),')
    lines.append('}')
    lines.append('')
    lines.append('# Per-country rate-type → rate. Used as fallback when a (dest, subcat) pair')
    lines.append('# is not in VAT_RATE_LOOKUP. Standard ("S") is the safe default.')
    lines.append('COUNTRY_RATES: dict[str, dict[str, float]] = {')
    for dest in sorted(country_rates.keys()):
        lines.append(f'    {dest!r}: {{')
        for rt in sorted(country_rates[dest].keys()):
            lines.append(f'        {rt!r}: {country_rates[dest][rt]:.4f},')
        lines.append('    },')
    lines.append('}')
    lines.append('')
    lines.append("# Convenience: each destination's standard rate.")
    lines.append('COUNTRY_STANDARD_RATE: dict[str, float] = {')
    for dest in sorted(country_rates.keys()):
        std = country_rates[dest].get("S")
        lines.append(f'    {dest!r}: {std:.4f},')
    lines.append('}')
    lines.append('')
    lines.append('# ── Sellers (non-EU, ship directly to EU consumers) ─────────────────────────')
    lines.append('# Each seller ships from one origin country to a fixed pair of destinations.')
    lines.append('# parent_categories lists the VAT_CATEGORIES the seller is observed selling.')
    lines.append('')
    lines.append('SELLERS: list[dict] = [')
    for s in sellers:
        lines.append('    {')
        lines.append(f'        "id":                {s["id"]!r},')
        lines.append(f'        "name":              {s["name"]!r},')
        lines.append(f'        "origin":            {s["origin"]!r},')
        lines.append(f'        "destinations":      {s["destinations"]!r},')
        lines.append(f'        "parent_categories": {s["parent_categories"]!r},')
        lines.append('    },')
    lines.append(']')
    lines.append('')
    lines.append('# ── Helpers ─────────────────────────────────────────────────────────────────')
    lines.append('')
    lines.append('def expected_rate_for(destination: str, subcategory_code: str) -> float | None:')
    lines.append('    """Return the canonical VAT rate (as fraction, e.g. 0.23) for this')
    lines.append('    (destination, subcategory) pair. Falls back to the country standard')
    lines.append('    rate if the exact pair is not in VAT_RATE_LOOKUP. Returns None only')
    lines.append('    if the destination itself is unknown."""')
    lines.append('    pair = VAT_RATE_LOOKUP.get((destination, subcategory_code))')
    lines.append('    if pair is not None:')
    lines.append('        return pair[0]')
    lines.append('    return COUNTRY_STANDARD_RATE.get(destination)')
    lines.append('')
    lines.append('')
    lines.append('def expected_rate_type_for(destination: str, subcategory_code: str) -> str | None:')
    lines.append('    """Return the VAT rate-type code ({S, R1, SR, Z}) for this pair, or')
    lines.append('    None on miss / unknown destination. Mirrors expected_rate_for shape."""')
    lines.append('    pair = VAT_RATE_LOOKUP.get((destination, subcategory_code))')
    lines.append('    return pair[1] if pair is not None else None')
    lines.append('')
    lines.append('')
    lines.append('def parent_category_for(subcategory_code: str) -> str | None:')
    lines.append('    """Look up the parent VAT category for a subcategory code."""')
    lines.append('    entry = SUBCATEGORY_BY_CODE.get(subcategory_code)')
    lines.append('    return entry[0] if entry else None')
    lines.append('')
    lines.append('')
    lines.append('def seller_by_id(seller_id: str) -> dict | None:')
    lines.append('    return next((s for s in SELLERS if s["id"] == seller_id), None)')
    lines.append('')
    lines.append('')
    lines.append('def seller_by_name(name: str) -> dict | None:')
    lines.append('    return next((s for s in SELLERS if s["name"] == name), None)')
    lines.append('')

    OUT_PATH.write_text("\n".join(lines))
    print(f"Wrote {OUT_PATH.relative_to(ROOT)}")
    print(f"  {len(parents)} parent categories, {sum(len(s) for s in parents.values())} subcategories")
    print(f"  {len(rate_lookup)} (dest, subcat) rate entries")
    print(f"  {len(sellers)} sellers")


if __name__ == "__main__":
    main()
